package cn.jbit;

import java.util.Scanner;

public class TestCalculator01 {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		Calculator01 calc = new Calculator01();
		System.out.println("输入第一个数:");
		calc.num1 = input.nextInt();
		System.out.println("输入第二个数:");
		calc.num2 = input.nextInt();
		System.out.println("两个相加是:" + calc.add());
		System.out.println("两个相减是:" + calc.minus());
		System.out.println("两个相乘是:" + calc.multiple());
		System.out.println("两个相除是:" + calc.divide());
	}
}
